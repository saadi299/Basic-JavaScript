
//javascript switch case break and default------------------------------
var color='black'
switch(color)
{
    case 'blue':
        console.log('I am blue');
        break;

    case 'green':
    console.log('I am green');
        break;

    case 'black':
    console.log('I am black');
        break;

    case 'red':
    console.log('I am red');
        break;

    default:
        console.log('I have no color');

}